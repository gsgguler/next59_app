-- ── WC2026 UEFA Qualifier Data ──────────────────────────────────────────────
-- Stores aggregate team stats and individual match results from the
-- UEFA WC2026 qualification campaign (groups + playoffs, ending Nov 2025 / Mar 2026)

CREATE TABLE IF NOT EXISTS wc2026_uefa_qualifier_team_stats (
  id                  uuid PRIMARY KEY DEFAULT gen_random_uuid(),
  team_name_tr        text NOT NULL,          -- original Turkish name from source
  team_name_en        text NOT NULL,          -- English canonical name
  api_football_team_id integer,               -- linked to wc2026_team_pool
  wc2026_qualified    boolean NOT NULL DEFAULT false,
  played              integer NOT NULL DEFAULT 0,
  wins                integer NOT NULL DEFAULT 0,
  draws               integer NOT NULL DEFAULT 0,
  losses              integer NOT NULL DEFAULT 0,
  goals_for           integer NOT NULL DEFAULT 0,
  goals_against       integer NOT NULL DEFAULT 0,
  goal_difference     integer NOT NULL DEFAULT 0,
  points              integer NOT NULL DEFAULT 0,
  win_rate            numeric(5,4) GENERATED ALWAYS AS (
    CASE WHEN played > 0 THEN ROUND(wins::numeric / played, 4) ELSE 0 END
  ) STORED,
  goals_per_game      numeric(5,3) GENERATED ALWAYS AS (
    CASE WHEN played > 0 THEN ROUND(goals_for::numeric / played, 3) ELSE 0 END
  ) STORED,
  gd_per_game         numeric(6,3) GENERATED ALWAYS AS (
    CASE WHEN played > 0 THEN ROUND(goal_difference::numeric / played, 3) ELSE 0 END
  ) STORED,
  created_at          timestamptz NOT NULL DEFAULT now()
);

ALTER TABLE wc2026_uefa_qualifier_team_stats ENABLE ROW LEVEL SECURITY;

CREATE POLICY "qualifier_team_stats_public_read" ON wc2026_uefa_qualifier_team_stats
  FOR SELECT TO anon, authenticated USING (true);

CREATE TABLE IF NOT EXISTS wc2026_uefa_qualifier_results (
  id               uuid PRIMARY KEY DEFAULT gen_random_uuid(),
  match_section    text,
  match_status     text,
  match_date       date,
  home_team_tr     text NOT NULL,
  away_team_tr     text NOT NULL,
  home_goals       integer,
  away_goals       integer,
  home_penalties   integer,
  away_penalties   integer,
  penalty_shootout boolean NOT NULL DEFAULT false,
  created_at       timestamptz NOT NULL DEFAULT now()
);

ALTER TABLE wc2026_uefa_qualifier_results ENABLE ROW LEVEL SECURITY;

CREATE POLICY "qualifier_results_public_read" ON wc2026_uefa_qualifier_results
  FOR SELECT TO anon, authenticated USING (true);

